export type McItem = {
  id: string;
  name: string;
  category: string;
  rarity: number; // 1 = most rare (shown first), higher = more common
};

export function mcItemIconUrl(itemId: string): string {
  const pascal = itemId
    .split("_")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("_");
  return `https://minecraft.wiki/images/Invicon_${pascal}.png`;
}

