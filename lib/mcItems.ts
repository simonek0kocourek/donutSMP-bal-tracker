// When no query: return all items sorted rarity asc (rarest first), capped at 20
// When query: fuzzy match, sort by match quality then rarity
export function searchMcItems(query: string): McItem[] {
  const q = query.toLowerCase().trim();
  if (!q) {
    return [...MC_ITEMS].sort((a, b) => a.rarity - b.rarity).slice(0, 20);
  }
  return MC_ITEMS
    .filter(
      (item) =>
        item.name.toLowerCase().includes(q) ||
        item.id.toLowerCase().includes(q) ||
        item.category.toLowerCase().includes(q),
    )
    .sort((a, b) => {
      // Exact name start > contains > rarity
      const aStart = a.name.toLowerCase().startsWith(q) ? 0 : 1;
      const bStart = b.name.toLowerCase().startsWith(q) ? 0 : 1;
      if (aStart !== bStart) return aStart - bStart;
      return a.rarity - b.rarity;
    })
    .slice(0, 20);
}
